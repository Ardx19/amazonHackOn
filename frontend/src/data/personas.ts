// Helper: build a CartItem shell from a product name + price
function makeOrderItem(productName: string, priceInr: number, imageUrl?: string): CartItem {
  return {
    product: {
      id: `seed-${productName.replace(/\s+/g, '-').toLowerCase().slice(0, 20)}`,
      name: productName,
      category: 'Ordered Item',
      categoryKey: 'ordered',
      subCategoryKey: 'seed',
      price: priceInr,
      originalPrice: priceInr,
      rating: 4.2,
      reviewCount: 120,
      imageUrl: imageUrl || 'https://images.unsplash.com/photo-1523275335684-37898b6baf30?w=400&auto=format&fit=crop&q=60',
      description: productName,
      features: [],
      inStock: true,
      brand: 'Amazon',
    },
    quantity: 1,
    selectedColor: 'Standard',
    selectedSize: 'Regular',
  };
}

// Helper: build a delivered order from seed purchase_history entry
function makeOrder(
  id: string,
  productName: string,
  priceInr: number,
  purchasedAt: string,
  shippingAddress: string,
  imageUrl?: string,
  status: Order['status'] = 'Delivered',
): Order {
  const date = new Date(purchasedAt);
  const formattedDate = date.toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' });
  return {
    id,
    orderDate: formattedDate,
    items: [makeOrderItem(productName, priceInr, imageUrl)],
    subtotal: priceInr,
    paymentMethod: 'Amazon Pay Balance',
    shippingAddress,
    status,
    expectedDelivery: 'Delivered',
  };
}

